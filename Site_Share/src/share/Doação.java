package share;

public class Doação {
	private String doacao_id;
	private int data_doacao;
	private String aparelho;
	private String marca;
	private String descricao;
	
	
	public String getDoacao_id() {
		return doacao_id;
	}
	public void setDoacao_id(String doacao_id) {
		this.doacao_id = doacao_id;
	}
	public int getData_doacao() {
		return data_doacao;
	}
	public void setData_doacao(int data_doacao) {
		this.data_doacao = data_doacao;
	}
	public String getAparelho() {
		return aparelho;
	}
	public void setAparelho(String aparelho) {
		this.aparelho = aparelho;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
